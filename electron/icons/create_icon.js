const fs = require('fs');
const path = require('path');

const icoHeader = Buffer.from([
  0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x10, 0x10,
  0x00, 0x00, 0x01, 0x00, 0x18, 0x00, 0x00, 0x00,
  0x16, 0x00, 0x00, 0x00
]);

const bmpHeader = Buffer.from([
  0x42, 0x4D, 0x36, 0x00, 0x00, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x36, 0x00, 0x00, 0x00, 0x28, 0x00,
  0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10, 0x00,
  0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xC4, 0x0E,
  0x00, 0x00, 0xC4, 0x0E, 0x00, 0x00, 0x00, 0x00,
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00
]);

const createIconData = () => {
  const data = [];
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const cx = 8, cy = 8;
      const dist = Math.sqrt(Math.pow(x - cx, 2) + Math.pow(y - cy, 2));
      if (dist < 6) {
        data.push(0x66, 0x7E, 0xEA, 0xFF);
      } else if (dist < 7) {
        data.push(0x76, 0x4B, 0xA2, 0xFF);
      } else {
        data.push(0x00, 0x00, 0x00, 0x00);
      }
    }
  }
  return Buffer.from(data);
};

const iconData = createIconData();
const icoData = Buffer.concat([icoHeader, bmpHeader, iconData]);

fs.writeFileSync(path.join(__dirname, 'app.ico'), icoData);
console.log('Icon created successfully');
